package komara.eo.jfx;

import javafx.geometry.Insets;
import javafx.geometry.Pos;
import javafx.scene.control.Label;
import javafx.scene.control.PasswordField;
import javafx.scene.control.Tab;
import javafx.scene.control.TextField;
import javafx.scene.layout.GridPane;
import javafx.scene.text.Font;
import javafx.scene.text.FontWeight;
import javafx.scene.text.Text;

import java.util.prefs.Preferences;

/**
 * Created by Rastislav Komara on 2/12/2015.
 */
public class HelloWorldApplicationModule implements ApplicationModule {

    @Override
    public Tab createModule(Preferences preferences) {
        Tab result = new Tab("Hello Wold");
        GridPane grid = new GridPane();
        grid.setAlignment(Pos.CENTER);
        grid.setHgap(10);
        grid.setVgap(10);
        grid.setPadding(new Insets(25, 25, 25, 25));

        Text sceneTitle = new Text("Welcome");
        String family = sceneTitle.getFont().getFamily();
        sceneTitle.setFont(Font.font(family, FontWeight.NORMAL, 20));
        grid.add(sceneTitle, 0, 0, 2, 1);

        Label userName = new Label("User Name:");
        grid.add(userName, 0, 1);

        TextField userTextField = new TextField();
        grid.add(userTextField, 1, 1);

        Label pw = new Label("Password:");
        grid.add(pw, 0, 2);

        PasswordField pwBox = new PasswordField();
        grid.add(pwBox, 1, 2);

        result.setContent(grid);
        return result;
    }

    @Override
    public String getModuleId() {
        return "Hello";
    }

    @Override
    public void savePreferences(Preferences preferences) {
        System.out.println("Good bye!");
    }
}
